    private String getWeatherData(String city) throws IOException {
    // URL API lengkap
    String urlString = "https://api.openweathermap.org/data/2.5/weather?q=" + city + "&appid=" + API_KEY + "&units=metric";
    
    // Membuka koneksi ke URL
    URL url = new URL(urlString);
    HttpURLConnection conn = (HttpURLConnection) url.openConnection();
    conn.setRequestMethod("GET");
    
    // Membaca hasil respon dari server (JSON)
    BufferedReader reader = new BufferedReader(new InputStreamReader(conn.getInputStream()));
    String line;
    StringBuilder response = new StringBuilder();
    
    while ((line = reader.readLine()) != null) {
        response.append(line);
    }
    reader.close();
    
    // Mengembalikan hasil JSON sebagai String
    return response.toString();
}

