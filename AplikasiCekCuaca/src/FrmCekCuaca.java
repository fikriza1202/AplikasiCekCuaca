    private void simpanCSV() {
    if (tblCuaca.getRowCount() == 0) {
        JOptionPane.showMessageDialog(this, "Tabel masih kosong! Tidak ada data yang disimpan.");
        return;
    }

    try (FileWriter writer = new FileWriter("dataCuaca.csv")) {
        for (int i = 0; i < tblCuaca.getRowCount(); i++) {
            for (int j = 0; j < tblCuaca.getColumnCount(); j++) {
                Object value = tblCuaca.getValueAt(i, j);
                if (value != null) {
                    writer.write(value.toString());
                } else {
                    writer.write(""); // isi kosong jika sel null
                }

                if (j < tblCuaca.getColumnCount() - 1) writer.write(",");
            }
            writer.write("\n");
        }
        JOptionPane.showMessageDialog(this, "Data berhasil disimpan ke file dataCuaca.csv!");
    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat menyimpan data: " + e.getMessage());
    }
}



private void muatCSV() {
    DefaultTableModel model = (DefaultTableModel) tblCuaca.getModel();
    model.setRowCount(0);
    try (BufferedReader br = new BufferedReader(new FileReader("dataCuaca.csv"))) {
        String line;
        while ((line = br.readLine()) != null) {
            model.addRow(line.split(","));
        }
        JOptionPane.showMessageDialog(this, "Data berhasil dimuat!");
    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Gagal memuat data!");
    }
}

    
    
    private void tampilkanCuaca(String json) {
    JSONObject obj = new JSONObject(json);
    JSONObject main = obj.getJSONObject("main");
    JSONArray weatherArray = obj.getJSONArray("weather");
    JSONObject weather = weatherArray.getJSONObject(0);

    double suhu = main.getDouble("temp");
    String kondisi = weather.getString("main");

    lblSuhu.setText("Suhu: " + suhu + " Â°C");
    lblKondisi.setText("Kondisi: " + kondisi);

    // Ganti ikon berdasarkan kondisi
    switch (kondisi.toLowerCase()) {
        case "rain":
            lblGambar.setIcon(new ImageIcon(getClass().getResource("/img/rain.png"))); break;
        case "clouds":
            lblGambar.setIcon(new ImageIcon(getClass().getResource("/img/cloud.jpg"))); break;
        case "clear":
            lblGambar.setIcon(new ImageIcon(getClass().getResource("/img/sun.png"))); break;
        default:
            lblGambar.setIcon(new ImageIcon(getClass().getResource("/img/unknown.png"))); break;
    }
}

    private void tambahKeTabel(String kota, String suhu, String kondisi) {
    DefaultTableModel model = (DefaultTableModel) tblCuaca.getModel();
    model.addRow(new Object[]{kota, suhu, kondisi});
}

